    <input type="hidden" name="_token" value="<?php echo csrf_token(); ?>"/>

    <script language="javascript">

        $(document).ready(function() {

            function getPVTList() {
                $('#pvt_list').DataTable({
                    iDisplayLength: 25,
                    processing: true,
                    serverSide: true,
                    lengthChange: false,
                    searching: true,
                    ajax: {
                        headers: {
                            'X-CSRF-TOKEN': '{{ csrf_token() }}'
                        },
                        url: '{{url("product-var-type/get-list")}}',
                        method: 'post'
                    },
                    columns: [
                        {data: 'name', name: 'name', searchable: true},
                        {data: 'description', name: 'description', searchable: true},
                        {data: 'action', name: 'action', orderable: false, searchable: false}
                    ],
                    "aaSorting": []
                });
            }

            getPVTList();

            $(document).on('click', '.store_new_pvt', function () {
                $('.add_response_msg_area').empty();
                var pvt_name = $('.pvt_name').val();
                var pvt_description = $('.pvt_description').val();

                if (pvt_name == '') {
                    alert("please select name");
                    return false;
                }
                if (pvt_description == '') {
                    alert("please insert description");
                    return false;
                }

                var btn = $(this);
                btn.prop('disabled', true);
                $('.spinner-icon').html('<span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span>');

                $.ajax({
                    url: '{{ url('/product-var-type/add-new-pvt') }}',
                    type: "POST",
                    //dataType: 'json',
                    headers: {
                        'X-CSRF-TOKEN': '{{ csrf_token() }}'
                    },
                    data: {
                        name: pvt_name,
                        description: pvt_description
                    },
                    success: function (response) {
                        btn.prop('disabled', false);
                        $('.spinner-icon').empty();

                        if (response.responseCode == 1) {
                            $('.add_response_msg_area').html('<div class="alert alert-success">\n' +
                                '                                <strong>Success!</strong> ' + response.message + '\n' +
                                '                            </div>');


                            $('.pvt_name').val('');
                            $('.pvt_description').val('');

                            $('.alert-success').fadeOut(3000);

                            setTimeout(function () {
                                $('#add-modal-lg').modal('hide');
                            }, 3200);

                            var dataTable = $('#pvt_list').dataTable();
                            dataTable.fnDestroy();
                            getPVTList();

                        } else {
                            $('.add_response_msg_area').html('<div class="alert alert-danger">\n' +
                                '                                <strong>Error!</strong> ' + response.message + '\n' +
                                '                            </div>');
                        }
                    },
                    error: function (jqXHR, textStatus, errorThrown) {

                    }
                });
            });


            $(document).on('click', '.open_pvt_modal', function () {

                var pvt_id = jQuery(this).data('pvt_id');

                var btn = $(this);
                btn.prop('disabled', true);

                $.ajax({
                    type: "POST",
                    dataType: "json",
                    url: "{{ url('/product-var-type/get-pvt-info') }}",
                    data: {
                        _token: $('input[name="_token"]').val(),
                        pvt_id: pvt_id
                    },
                    success: function (response) {
                        btn.prop('disabled', false);
                        if(response.responseCode == 1){
                            $('.edit_data_content').html(response.html);
                            $('#edit-modal-lg').modal();
                        }else{

                        }
                    }
                });

            });


            $(document).on('click', '.update_pvt', function () {
                $('.edit_response_msg_area').empty();
                var edit_pvt_id = $('.edit_pvt_id').val();
                var edit_pvt_name = $('.edit_pvt_name').val();
                var edit_pvt_description = $('.edit_pvt_description').val();

                if (edit_pvt_name == '') {
                    alert("please insert name");
                    return false;
                }
                if (edit_pvt_description == '') {
                    alert("please insert description");
                    return false;
                }

                var btn = $(this);
                btn.prop('disabled', true);
                $('.spinner-icon').html('<span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span>');

                $.ajax({
                    url: '{{ url('/product-var-type/update-pvt') }}',
                    type: "POST",
                    //dataType: 'json',
                    headers: {
                        'X-CSRF-TOKEN': '{{ csrf_token() }}'
                    },
                    data: {
                        pvt_id: edit_pvt_id,
                        pvt_name: edit_pvt_name,
                        pvt_description: edit_pvt_description
                    },
                    success: function (response) {
                        btn.prop('disabled', false);
                        $('.spinner-icon').empty();

                        if (response.responseCode == 1) {
                            $('.edit_response_msg_area').html('<div class="alert alert-success">\n' +
                                '                                <strong>Success!</strong> ' + response.message + '\n' +
                                '                            </div>');

                            $('.alert-success').fadeOut(3000);

                            setTimeout(function () {
                                $('#edit-modal-lg').modal('hide');
                            }, 3200);

                            var dataTable = $('#pvt_list').dataTable();
                            dataTable.fnDestroy();
                            getPVTList();

                        } else {
                            $('.edit_response_msg_area').html('<div class="alert alert-danger">\n' +
                                '                                <strong>Error!</strong> ' + response.message + '\n' +
                                '                            </div>');
                        }
                    },
                    error: function (jqXHR, textStatus, errorThrown) {

                    }
                });
            });


            $(document).on('click', '.deletePVT', function () {

                var result = confirm("Want to delete?");
                if (!result) {
                    return false;
                }

                var pvt_id = jQuery(this).data('pvt_id');

                var btn = $(this);
                btn.prop('disabled', true);

                $.ajax({
                    type: "POST",
                    dataType: "json",
                    url: "{{ url('/product-var-type/delete-pvt') }}",
                    data: {
                        _token: $('input[name="_token"]').val(),
                        pvt_id: pvt_id
                    },
                    success: function (response) {
                        btn.prop('disabled', false);
                        if(response.responseCode == 1){
                            $('.delete_response_msg_area').html('<div class="alert alert-success">\n' +
                                '                                <strong>Success!</strong> ' + response.message + '\n' +
                                '                            </div>');

                            $('.alert-success').fadeOut(3000);

                            setTimeout(function () {
                                $('#add-modal-lg').modal('hide');
                            }, 3200);

                            var dataTable = $('#pvt_list').dataTable();
                            dataTable.fnDestroy();
                            getPVTList();
                        }else{

                        }
                    }
                });

            });

        });

    </script>
@endsection
